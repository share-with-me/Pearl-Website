window.document.onkeydown = function (e)
{
    if (!e){
        e = event;
    }
    if (e.keyCode == 27){
        lightbox_close0();
    }
}

function lightbox_open0(){
    window.scrollTo(0,0);
    document.getElementById('light0').style.display='block';
    document.getElementById('fade0').style.display='block';  
}
